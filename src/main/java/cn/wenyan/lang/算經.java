package cn.wenyan.lang;

public class 算經 {

    public static double 正弦(double i){
        return Math.sin(i);
    }

    public static double 餘弦(double i){
        return Math.cos(i);
    }

    public static double 絕對(double i){
        return Math.abs(i);
    }

    public static double 平方根(double i){
        return Math.sqrt(i);
    }

}
